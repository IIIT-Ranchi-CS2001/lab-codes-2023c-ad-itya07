# Q1: Basic Arithmetic Operations
a, b = map(float, input("Enter two numbers separated by space: ").split())
print(f"Sum: {a + b}")
print(f"Difference: {a - b}")
print(f"Product: {a * b}")
print(f"Integer Quotient: {a // b}")
print(f"Remainder: {a % b}")
print(f"Fractional Quotient: {a / b}")